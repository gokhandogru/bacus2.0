<h5>Objective of the Project</h5>

<div class='card-panel teal lighten-2'>


  <p><b>BACUS</b> is a long lasting, multilingual terminology project started in UAB.
  Over the years, created terminology entries have been hosted in old server. In order to revive the legacy of the more 20 years of terminology work, we have started the BACUS 2.0 project. The primary objective has been to publish the same content with user-friendly and mobile-friendly, agile interface. We have also realized all the details of our website architecture with a Creative Commons license on GitHub so that anyone who would like to create a multilingual website can make use of it.</a></p>

    <h6>BACUS Project</h6>
    <p>BACUS is a database born with the will of constituting a resource for the access and dissemination of the terminology works made by the students of the Faculty of Translation and Interpreting at the Autonomous University of Barcelona.
From 1995 and within the framework of the Terminology Applied to Translation class, our students have done terminology research of multilingual character in several fields of specialty. These works are presented now in digital version for free access of all the Internet community with the aim of becoming a useful tool for different groups (proof-readers, translators, specialists, etc.).
Every project has been supervised by an expert on the subject and has been developed at least in three languages; chosen among the languages offered in our degree (Arabic, Catalan, Chinese, English, German, Italian, Japanese, Portuguese, Russian, Turkish and Spanish). The current web interface includes 21 languages, however, the database files includes more languages.</p>
